/**
 * 获取农历日期显示文字
 */
export function getLunarDay(date: string): string | undefined {
  const lunar = lunarData[date];
  if (!lunar) return undefined;
  
  const [month, day] = lunar;
  // 初一显示月份名
  if (day === 1) {
    return LUNAR_MONTHS[month - 1] + '月';
  }
  return LUNAR_DAYS[day];
}

