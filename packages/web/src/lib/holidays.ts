/**
 * 判断指定日期是否为法定节假日
 */
export function isHoliday(date: string): boolean {
  const info = getHolidayInfo(date);
  return !!info && !info.isWorkday;
}

/**
 * 判断指定日期是否为调休工作日
 */
export function isWorkday(date: string): boolean {
  const info = getHolidayInfo(date);
  return !!info?.isWorkday;
}

/**
 * 获取日期下方显示的标签
 * 优先级：节假日第一天名称 > 休/班 > 节气
 */
export function getDateLabel(date: string): { 
  label: string; 
  type: 'holiday-name' | 'holiday' | 'workday' | 'solar-term' | 'lunar';
  fullName?: string;
} | undefined {
  const holidayInfo = getHolidayInfo(date);
  const solarTermInfo = getSolarTermInfo(date);
  
  // 1. 节假日优先
  if (holidayInfo) {
    if (holidayInfo.isWorkday) {
      return { label: '班', type: 'workday', fullName: holidayInfo.name };
    }
    if (holidayInfo.isFirst) {
      // 节假日第一天显示具体名称
      return { label: holidayInfo.name, type: 'holiday-name', fullName: holidayInfo.name };
    }
    return { label: '休', type: 'holiday', fullName: holidayInfo.name };
  }
  
  // 2. 节气优先
  if (solarTermInfo) {
    return { label: solarTermInfo.name, type: 'solar-term' };
  }
  
  // 3. 农历日期
  const lunarDay = getLunarDay(date);
  if (lunarDay) {
    return { label: lunarDay, type: 'lunar' };
  }
  
  return undefined;
}

/**
 * 获取节假日名称（用于 tooltip）
 */
export function getHolidayName(date: string): string | undefined {
  const holidayInfo = getHolidayInfo(date);
  if (holidayInfo) return holidayInfo.name;
  
  const solarTermInfo = getSolarTermInfo(date);
  if (solarTermInfo) return solarTermInfo.name;
  
  return undefined;
}

// 保留旧的 API 兼容性
export function getHolidayLabel(date: string) {
  const result = getDateLabel(date);
  if (!result) return undefined;
  if (result.type === 'solar-term') return undefined;
  return {
    label: result.label,
    type: result.type === 'workday' ? 'workday' as const : 'holiday' as const,
  };
}
